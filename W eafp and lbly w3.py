#EAFP:‘Easier to Ask for Forgiveness than Permission’ 
#EAFP is a piece of gem advice in the Python community which may not be familiar to programmers of other communities.
#Quickly, EAFP or ‘Easier to Ask for Forgiveness than Permission’ means holding an optimistic view for the code you are working on,
#and if an exception is thrown catch it and deal with it later.

