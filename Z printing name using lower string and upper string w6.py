#PRINTING NAME USING LOWER AND UPPER STRING
#lower string:The lower() method returns the string in lower case
#upper string:The upper() method returns the string in upper case
#input used:For this purpose, Python provides the function input(). input has an optional parameter, which is the prompt string.
#If the input function is called, the program flow will be stopped until the user has given an input and has ended the input with the return key.

name=input("enter your name:")
print("printing name in lower case: ",name.lower())
print("printing name in upper case: ",name.upper())
